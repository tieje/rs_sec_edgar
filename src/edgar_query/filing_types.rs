#[cfg(test)]
mod tests {
    use super::*;
    #[test]
    fn test_valid_filing_type_from_str() {
        assert_eq!(Filing::from_str("X-17F-1A"), FilingType::X17F1A)
    }
    #[test]
    #[should_panic]
    fn test_invalid_filing_type_from_str() {
        let _unused = Filing::from_str("not a real filing type");
        assert!(false)
    }
    #[test]
    fn test_filing_type_to_string() {
        assert_eq!(
            Filing::to_string(FilingType::X17F1A),
            "X-17F-1A".to_string()
        )
    }
    #[test]
    fn test_valid_validate_filing_type() {
        let filing = "X-17F-1A";
        assert_eq!(
            Filing::validate_filing_type_string(&filing),
            filing.to_string()
        )
    }
    #[test]
    #[should_panic]
    fn test_invalid_validate_filing_type() {
        let filing = "not real filing type";
        assert_eq!(
            Filing::validate_filing_type_string(&filing),
            filing.to_string()
        )
    }
}
